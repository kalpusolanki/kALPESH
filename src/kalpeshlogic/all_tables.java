package kalpeshlogic;

public class all_tables 
{
	public static void main(String[] args)
	{
		int i=2;
		for (int k=1;k<=10;k++)
		{
			System.out.println(i*k);
		}
	System.out.println("===for 17 table==========");	
	int n=17;
	for(int g=1;g<=10;g++)
	{
		System.out.println(g*n);
	}
	
	System.out.println("====for all table upto 20======");
		
	for(int m=1;m<=20;m++) 
	{
		for(int f=1;f<=10;f++) 
		{
			System.out.print(m*f +" ");
		}
		System.out.println();
		
	}
		
		
	}

}
