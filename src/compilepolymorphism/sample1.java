package compilepolymorphism;

public class sample1
{
// method overloading
	//Declaring multiple method with same method name but with differnt arguement/input/parameter in a same class is method overloading
	
	
	// addition method with 2 integer parameter
	//Declaring multiple method with same method name but with different 
	//argument/parameter/inputs in a same class is called method overloading
	public void addition(int num1,int num2) 
	{
		System.out.println(num1+num2);
	}
	
	public void addition(int num1,int num2,int num3)
	{
		System.out.println(num1+num2);
	}
	
	
	
	
	
}
